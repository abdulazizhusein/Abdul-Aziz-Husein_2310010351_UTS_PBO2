    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                     
    String judul = jTextField1.getText();
    String tanggal = jTextField2.getText();
    String isi = jTextArea1.getText();

    if (judul.isEmpty() || isi.isEmpty() || tanggal.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!");
        return;
    }

    controller.tambah(judul, isi, tanggal);

    JOptionPane.showMessageDialog(this, "Catatan berhasil ditambahkan!");

    tampilkanData();
    resetForm();
 

   
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                              
    int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih catatan dulu!");
        return;
    }

    int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
    String judul = jTextField1.getText();
    String tanggal = jTextField2.getText();
    String isi = jTextArea1.getText();

    controller.ubah(id, judul, isi, tanggal);

    JOptionPane.showMessageDialog(this, "Catatan berhasil diperbarui!");

    tampilkanData();
    resetForm();                   
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                                               
    int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih catatan yang ingin dihapus!");
        return;
    }

    int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

    controller.hapus(id);

    JOptionPane.showMessageDialog(this, "Catatan berhasil dihapus!");

    tampilkanData();
    resetForm();
    }//GEN-LAST:event_jButton3ActionPerformed

