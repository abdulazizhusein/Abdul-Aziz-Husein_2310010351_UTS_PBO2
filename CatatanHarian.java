    private void tampilkanData() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    for (Catatan c : controller.tampilSemua()) {
        model.addRow(new Object[]{
            c.getId(),
            c.getJudul(),
            c.getTanggal(),
            c.getIsi()
        });
    }
}
